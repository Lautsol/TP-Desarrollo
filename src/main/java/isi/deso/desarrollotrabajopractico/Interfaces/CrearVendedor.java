    public boolean confirmarAccion() {
        String[] opciones = {"Aceptar", "Cancelar"};
        int opcion = JOptionPane.showOptionDialog(
                null,
                "¿Está seguro de que desea guardar el vendedor?",
                "Confirmar acción",
                JOptionPane.DEFAULT_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                opciones,
                opciones[0] 
        );
        return opcion == 0; 
    }
    
